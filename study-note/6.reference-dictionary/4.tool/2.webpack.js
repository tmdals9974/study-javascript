/****************************************************************
 * * Webpack Concepts
 * 
 * * Entry : 진입점 파일 (시작 파일)
 * * Output : 결과물 저장 정보
 * * Loaders : 입력된 파일들을 처리할 모듈(트랜스 파일러, CSS 변환 등)을 Loader 형식으로 만들어서 주입할 수 있음.
 * * Plugins : Loader가 처리한 파일을 출력할 모듈(압축, 이미지 최적화 등)
 * 
 * * webpack boilerplate 로 검색하여 다른 설정 예제를 확인하는 것을 추천.
 ****************************************************************/